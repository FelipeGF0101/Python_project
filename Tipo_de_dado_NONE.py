"""
TIPO NONE

O tipo de dado NONE em Python representa o tipo sem tipo, ou poderia ser conhecido também como tipo vazio. Porém,
falar que é um tipo sem tipo é mais apropriado.

OBS: O tipo None é sempre especificado com a primeira letra maiúcula.

Certo: None
Errado: none

QUANDO UTILIZAMOS?

- Podemos utilizar None quando queremos criar uma variável e inicializá-la com um tipo sem tipo, antes de recebermos
um valor final.

Obs: O tipo None em Python é sempre considerado false.
"""
numero = None

print(numero)
print(type(numero))
